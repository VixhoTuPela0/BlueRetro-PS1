void ssd1306_draw_char(uint8_t x, uint8_t y, char c)
{
    if ((uint8_t)c < 0x20 || (uint8_t)c > 0x7E) {
        return;
    }
    const uint8_t *glyph = font5x8[(uint8_t)c - 0x20];
    for (int col = 0; col < 5; col++) {
        uint8_t col_data = glyph[col];
        for (int row = 0; row < 8; row++) {
            ssd1306_draw_pixel((uint8_t)(x + col), (uint8_t)(y + row),
                               (col_data >> row) & 1);
        }
    }
}

uint8_t ssd1306_draw_string(uint8_t x, uint8_t y, const char *str, uint8_t max_w)
{
    uint8_t cx = x;
    while (*str) {
        if (cx + 6 > x + max_w) {
            break;
        }
        ssd1306_draw_char(cx, y, *str);
        cx += 6;
        str++;
    }
    return cx;
}

void ssd1306_draw_bitmap(uint8_t x, uint8_t y,
                          const uint8_t *bmp, uint8_t w, uint8_t h)
{
    /* bytes per row in the source bitmap */
    uint8_t row_bytes = (w + 7) / 8;
    for (int row = 0; row < h; row++) {
        for (int col = 0; col < w; col++) {
            int byte_idx = row * row_bytes + col / 8;
            int bit_idx  = 7 - (col % 8);   /* MSB = leftmost */
            uint8_t on   = (bmp[byte_idx] >> bit_idx) & 1;
            ssd1306_draw_pixel((uint8_t)(x + col), (uint8_t)(y + row), on);
        }
    }
}
