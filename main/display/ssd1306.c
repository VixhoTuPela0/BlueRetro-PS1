int ssd1306_init(void)
{
    esp_err_t ret;

    /* Optional hardware reset */
#if OLED_RST_PIN >= 0
    gpio_config_t rst_conf = {
        .pin_bit_mask = 1ULL << OLED_RST_PIN,
        .mode = GPIO_MODE_OUTPUT,
        .pull_up_en = GPIO_PULLUP_DISABLE,
        .pull_down_en = GPIO_PULLDOWN_DISABLE,
        .intr_type = GPIO_INTR_DISABLE,
    };
    gpio_config(&rst_conf);
    gpio_set_level(OLED_RST_PIN, 0);
    vTaskDelay(pdMS_TO_TICKS(10));
    gpio_set_level(OLED_RST_PIN, 1);
    vTaskDelay(pdMS_TO_TICKS(10));
#endif

    /* Configure I2C master */
    i2c_config_t conf = {
        .mode = I2C_MODE_MASTER,
        .sda_io_num = OLED_SDA_PIN,
        .scl_io_num = OLED_SCL_PIN,
        .sda_pullup_en = GPIO_PULLUP_ENABLE,
        .scl_pullup_en = GPIO_PULLUP_ENABLE,
        .master.clk_speed = OLED_FREQ_HZ,
    };
    ret = i2c_param_config(OLED_I2C_PORT, &conf);
    if (ret != ESP_OK) {
        printf("# ssd1306: i2c_param_config failed: %d\n", ret);
        return -1;
    }
    ret = i2c_driver_install(OLED_I2C_PORT, I2C_MODE_MASTER, 0, 0, 0);
    if (ret != ESP_OK) {
        printf("# ssd1306: i2c_driver_install failed: %d\n", ret);
        return -1;
    }

    vTaskDelay(pdMS_TO_TICKS(5));

    /* Send initialisation sequence */
    ret = send_cmds(init_cmds, sizeof(init_cmds));
    if (ret != ESP_OK) {
        printf("# ssd1306: init cmds failed: %d\n", ret);
        return -1;
    }

    ssd1306_clear();
    ssd1306_update();
    return 0;
}

void ssd1306_clear(void)
{
    memset(fb, 0, sizeof(fb));
}

void ssd1306_update(void)
{
    /* Set column and page address to cover the whole display */
    uint8_t addr_cmds[] = {
        0x21, 0x00, 0x7F, /* column address: 0–127 */
        0x22, 0x00, 0x07, /* page address:   0–7   */
    };
    send_cmds(addr_cmds, sizeof(addr_cmds));

    /* Send all pages row-by-row using the DATA control byte.
     * We split into page-sized chunks so each I2C transaction stays
     * within the driver's internal buffer limits.                     */
    for (int page = 0; page < OLED_PAGES; page++) {
        i2c_cmd_handle_t cmd = i2c_cmd_link_create();
        i2c_master_start(cmd);
        i2c_master_write_byte(cmd, (OLED_I2C_ADDR << 1) | I2C_MASTER_WRITE, true);
        i2c_master_write_byte(cmd, OLED_DATA_BYTE, true);
        i2c_master_write(cmd, fb[page], OLED_WIDTH, true);
        i2c_master_stop(cmd);
        i2c_master_cmd_begin(OLED_I2C_PORT, cmd, pdMS_TO_TICKS(100));
        i2c_cmd_link_delete(cmd);
    }
}

void ssd1306_draw_pixel(uint8_t x, uint8_t y, uint8_t on)
{
    if (x >= OLED_WIDTH || y >= OLED_HEIGHT) {
        return;
    }
    uint8_t page = y / 8;
    uint8_t bit  = y % 8;
    if (on) {
        fb[page][x] |= (uint8_t)(1 << bit);
    } else {
        fb[page][x] &= (uint8_t)~(1 << bit);
    }
}

