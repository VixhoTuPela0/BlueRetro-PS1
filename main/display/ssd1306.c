/* ------------------------------------------------------------------ */
/*  I2C transport                                                       */
/* ------------------------------------------------------------------ */

#define OLED_I2C_PORT   I2C_NUM_0
#define OLED_FREQ_HZ    400000
#define OLED_CMD_BYTE   0x00  /* Co=0, D/C#=0 */
#define OLED_DATA_BYTE  0x40  /* Co=0, D/C#=1 */

/* framebuffer: 8 pages × 128 columns */
static uint8_t fb[OLED_PAGES][OLED_WIDTH];

/* ------------------------------------------------------------------ */
/*  SSD1306 command list (128×64, charge-pump, horizontal addressing)  */
/* ------------------------------------------------------------------ */
static const uint8_t init_cmds[] = {
    0xAE,       /* display off */
    0xD5, 0x80, /* set display clock divide ratio / oscillator freq */
    0xA8, 0x3F, /* set multiplex ratio (63 → 64 rows) */
    0xD3, 0x00, /* set display offset */
    0x40,       /* set display start line = 0 */
    0x8D, 0x14, /* charge pump: enable */
    0x20, 0x00, /* memory addressing mode: horizontal */
    0xA1,       /* segment remap (col 127 → SEG0) */
    0xC8,       /* COM output scan direction: remapped */
    0xDA, 0x12, /* COM pins hardware configuration */
    0x81, 0xCF, /* set contrast */
    0xD9, 0xF1, /* set pre-charge period */
    0xDB, 0x40, /* set VCOMH deselect level */
    0xA4,       /* entire display on (resume from RAM) */
    0xA6,       /* normal display (not inversed) */
    0xAF,       /* display on */
};

/* ------------------------------------------------------------------ */
/*  Low-level helpers                                                   */
/* ------------------------------------------------------------------ */

static esp_err_t i2c_write(uint8_t ctrl, const uint8_t *data, size_t len)
{
    i2c_cmd_handle_t cmd = i2c_cmd_link_create();
    i2c_master_start(cmd);
    i2c_master_write_byte(cmd, (OLED_I2C_ADDR << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(cmd, ctrl, true);
    if (data && len) {
        i2c_master_write(cmd, (uint8_t *)data, len, true);
    }
    i2c_master_stop(cmd);
    esp_err_t ret = i2c_master_cmd_begin(OLED_I2C_PORT, cmd, pdMS_TO_TICKS(50));
    i2c_cmd_link_delete(cmd);
    return ret;
}

static inline esp_err_t send_cmd(uint8_t c)
{
    return i2c_write(OLED_CMD_BYTE, &c, 1);
}

static inline esp_err_t send_cmds(const uint8_t *buf, size_t len)
{
    return i2c_write(OLED_CMD_BYTE, buf, len);
}

